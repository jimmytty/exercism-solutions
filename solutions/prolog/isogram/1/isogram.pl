isogram(Phrase) :-
    string_lower(Phrase, S),
    not(wildcard_match('*a*a*', S)),
    not(wildcard_match('*b*b*', S)),
    not(wildcard_match('*c*c*', S)),
    not(wildcard_match('*d*d*', S)),
    not(wildcard_match('*e*e*', S)),
    not(wildcard_match('*f*f*', S)),
    not(wildcard_match('*g*g*', S)),
    not(wildcard_match('*h*h*', S)),
    not(wildcard_match('*i*i*', S)),
    not(wildcard_match('*j*j*', S)),
    not(wildcard_match('*k*k*', S)),
    not(wildcard_match('*l*l*', S)),
    not(wildcard_match('*m*m*', S)),
    not(wildcard_match('*n*n*', S)),
    not(wildcard_match('*o*o*', S)),
    not(wildcard_match('*p*p*', S)),
    not(wildcard_match('*q*q*', S)),
    not(wildcard_match('*r*r*', S)),
    not(wildcard_match('*s*s*', S)),
    not(wildcard_match('*t*t*', S)),
    not(wildcard_match('*u*u*', S)),
    not(wildcard_match('*v*v*', S)),
    not(wildcard_match('*w*w*', S)),
    not(wildcard_match('*x*x*', S)),
    not(wildcard_match('*y*y*', S)),
    not(wildcard_match('*z*z*', S))
.
